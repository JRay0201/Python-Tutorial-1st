#字符串索引：对字符串中某个字符的检索称为索引
#语法结构： 字符串 或  字符串变量[序号]

#-5 -4 -3 -2 -1  反向递减序列
# H  E  L  L  O
# 0  1  2  3  4  正向递增序列

s = 'HELLOWORLD'
print(s[0],s[-10])

print('JRay'[3])
print('JRay'[-1])

#字符串切片：对字符串中某个字串或区片的检索称为切片
#语法结构： 字符串或者字符串变量[N:M]
#切片获取字符串中从N 到 M （不包含M）的子字符串
print('ARailay'[1:4])
print(s[-8:-3])
print(s[:5]) #N 默认从0开始
print(s[5:]) #M 默认切到字符串结尾

